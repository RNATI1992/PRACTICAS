public class ejercicio_1 {
    public static void main(String[] args) {
        System.out.println("Operaciones con enteros");
        int ia = 7, ib = 3;
        System.out.println("El resultado de la suma es " + ia+ib);
        System.out.println("El resultado del producto es " + ia*ib);
        System.out.println("El resultado del cociente es " + ia/ib);
        System.out.println("El resto de la división entera es " + ia%ib);
        System.out.println("**********************************");
        System.out.println("Operaciones con números decimales");
        double da = 7.5, db = 3.0;
        System.out.println("El resultado de la suma es " + da + db);
        System.out.println("El resultado del producto es " + da * db);
        System.out.println("El resultado del cociente es " + da / db);
        System.out.println("El resto de la división es " + da % db);
    }
}